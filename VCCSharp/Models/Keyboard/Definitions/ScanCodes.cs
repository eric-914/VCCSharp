namespace VCCSharp.Models.Keyboard.Definitions
{
    public static class ScanCodes
    {
        public const char Null = (char)0x00;   

        public const char D1 = (char)0x01;   // 1,!
        public const char D2 = (char)0x03;   // 2,@
        public const char D3 = (char)0x04;   // 3,#
        public const char D4 = (char)0x05;   // 4,$
        public const char D5 = (char)0x06;   // 5,%
        public const char D6 = (char)0x07;   // 6,^
        public const char D7 = (char)0x08;   // 7,&
        public const char D8 = (char)0x09;   // 8,*
        public const char D9 = (char)0x0A;   // 9,(
        public const char D0 = (char)0x0B;   // 0,)

        public const char A = (char)0x1E;
        public const char B = (char)0x30;
        public const char C = (char)0x2E;
        public const char D = (char)0x20;
        public const char E = (char)0x12;
        public const char F = (char)0x21;
        public const char G = (char)0x22;
        public const char H = (char)0x23;
        public const char I = (char)0x17;
        public const char J = (char)0x24;
        public const char K = (char)0x25;
        public const char L = (char)0x26;
        public const char M = (char)0x32;
        public const char N = (char)0x31;
        public const char O = (char)0x18;
        public const char P = (char)0x19;
        public const char Q = (char)0x10;
        public const char R = (char)0x13;
        public const char S = (char)0x1F;
        public const char T = (char)0x14;
        public const char U = (char)0x16;
        public const char V = (char)0x2F;
        public const char W = (char)0x11;
        public const char X = (char)0x2D;
        public const char Y = (char)0x15;
        public const char Z = (char)0x2C;

        public const char Space = (char)0x39;

        public const char Minus = (char)0x0C;       // -,_
        public const char Plus = (char)0x0D;        // =,+
        public const char LBracket = (char)0x1A;    // [,{
        public const char RBracket = (char)0x1B;    // ],}
        public const char Semicolon = (char)0x27;   // ;,:
        public const char Quotes = (char)0x28;      // ',"
        public const char Tilde = (char)0x29;       // `,~
        public const char Slash = (char)0x35;       // /,?
        public const char Comma = (char)0x33;       // ,,<
        public const char Period = (char)0x34;      // .,>
        public const char Backslash = (char)0x2B;   // \,|

        public const char Return = (char)0x1C;      // \n - Enter
        public const char Tab = (char)0x39;         // \t - Tab

        public const char Undefined = (char)0xFF;
    }
}
